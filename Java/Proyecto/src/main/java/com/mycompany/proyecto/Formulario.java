    private void mostrarValoresconfigurados() {
        try (ObjectInputStream flujoEntrada = new ObjectInputStream(new FileInputStream("src\\main\\java\\com\\mycompany\\proyecto\\config.ini")) {
        }) {
            String ip = "";
            String usuario = "";
            String contraseña = "";
            String Driver = "";
            String Puerto = "";
            String bdd = "";
            try {
                ip = (String) flujoEntrada.readObject();
                usuario = (String) flujoEntrada.readObject();
                contraseña = (String) flujoEntrada.readObject();
                Driver = (String) flujoEntrada.readObject();
                Puerto = (String) flujoEntrada.readObject();
                bdd = (String) flujoEntrada.readObject();
            } catch (IOException e) {
                System.out.println(e);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Formulario.class.getName()).log(Level.SEVERE, null, ex);
            }
            IPjTextField.setText(ip);
            UsuariojTextField.setText(usuario);
            ContraseñajTextField.setText(contraseña);
            DriverjTextField.setText(Driver);
            PuertojTextField.setText(Puerto);
            BaseDeDatosjTextField.setText(bdd);
        } catch (IOException e) {
            System.out.println(e);
        }
    }

