    private void botonAnadirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonAnadirMouseClicked
        // TODO add your handling code here:
        String palabra = entradaPalabras.getText();
        
        if (this.mapaPalabras.containsKey(palabra))
            {
                //actualizar el valor de la frecuencia
                Integer valor = this.mapaPalabras.get(palabra);
                valor++;
                this.mapaPalabras.put(palabra, valor);
            }
            else
            {
                //añadir la nueva palabra
                this.mapaPalabras.put(palabra, 1);
            }
        salidaMapa.setText(toString(this.mapaPalabras));
    }//GEN-LAST:event_botonAnadirMouseClicked

    private void botonBuscarFrecuenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBuscarFrecuenciaMouseClicked
        // TODO add your handling code here:
        //leer el numero y pasarlo a int
        String num = entradaFrecuencia.getText();
        int numero = Integer.parseInt(num);
        
        //invertir los valores y key del mapa
        //mapa nuevo para rellenar con los valores y key invertidos
        Map <Integer, String> mapaFrecuencia = new HashMap<>();
        //iterador para el primer mapa
        for (Map.Entry<String, Integer> entry : this.mapaPalabras.entrySet())
        {
            String palabra = entry.getKey();
            Integer frecuencia = entry.getValue();
            if(mapaFrecuencia.containsKey(frecuencia))
            {
                String contenido = mapaFrecuencia.get(frecuencia);
                palabra = palabra + " " + contenido;
                mapaFrecuencia.put(frecuencia, palabra);
            }
            //frecuenciaPalabrasInverso.put(frec, word);
            mapaFrecuencia.put(frecuencia, palabra);
        }
        
        //añadir los valores de los key que se correspondan con numero a una lista
        List <String> lista = new ArrayList();
        for (Map.Entry<Integer, String> entry2 : mapaFrecuencia.entrySet())
        {
            Integer frecuencia = entry2.getKey();
            if(frecuencia == numero)
            {
                String palabra = entry2.getValue();
                lista.add(palabra);
            }
        }
        
        salidaPalabrasFrecuencia.setText(toString(lista));
        
    }//GEN-LAST:event_botonBuscarFrecuenciaMouseClicked

