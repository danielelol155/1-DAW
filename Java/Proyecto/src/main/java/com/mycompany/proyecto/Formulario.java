    private void AplicarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AplicarjButtonActionPerformed
        // TODO add your handling code here:
        ObjectOutputStream flujosalida = null;

        try {
            String ip = IPjTextField.getText() + "";
            String usuario = UsuariojTextField.getText() + "";
            String contraseña = ContraseñajTextField.getText() + "";
            String Driver = DriverjTextField.getText() + "";
            String Puerto = PuertojTextField.getText() + "";
            String bdd = BaseDeDatosjTextField.getText() + "";

            flujosalida = new ObjectOutputStream(new FileOutputStream("src\\main\\java\\com\\mycompany\\proyecto\\config.ini"));
            flujosalida.writeObject(ip);
            flujosalida.writeObject(usuario);
            flujosalida.writeObject(contraseña);
            flujosalida.writeObject(Driver);
            flujosalida.writeObject(Puerto);
            flujosalida.writeObject(bdd);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } finally {
            JOptionPane.showMessageDialog(rootPane, "Cambios aplicados");
            System.out.println("trabajo terminado");
            if (flujosalida != null) {
                try {
                    flujosalida.close();
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(rootPane, e);
                }
            }
        }
    }//GEN-LAST:event_AplicarjButtonActionPerformed

