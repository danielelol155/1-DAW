    public String toString (Map<String, Integer> mapa)
    {
        String cad = "";
        
        for (Map.Entry<String, Integer> entry : mapa.entrySet())
        {
            String palabra = entry.getKey();
            Integer frecuencia = entry.getValue();
            cad = cad + palabra + " ->" + frecuencia + ";";
        }
        
        return cad;
    }
    public String toString (List <String> lista)
    {
        String cad= "";
        Iterator <String> it = lista.iterator();
        for(;it.hasNext();)
        {
            String palabra = it.next();
            cad = cad + " " + palabra;
        }
        return cad;
    }
